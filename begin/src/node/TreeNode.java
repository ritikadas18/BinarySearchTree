package node;

public class TreeNode {
    private int data;
    private TreeNode left;
    private TreeNode right;

    public TreeNode(int data) {
        this.data=data;
        this.left=left;
        this.right=right;
    }
    public int getData() {
        return data;
    }

    public void setData(int data) {
        this.data = data;
    }
}
